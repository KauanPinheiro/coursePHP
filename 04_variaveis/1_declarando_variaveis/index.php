<!-- 
    SOBRE AS VARIÁVEIS
são a forma que temos para declarar um valor e salvá-los na memória
uma variável em php tem o $ na frente do seu nome

ex: $nome = "Isabela";

podemos salvar qualquer tipo de dado 
podemos alterar o valor de uma variavel no decorrer do programa 
podemos imprimir o valor de uma variável com echo 


-->

<?php

$test = "Text";

echo $test;

